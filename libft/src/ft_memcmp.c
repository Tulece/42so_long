#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t len)
{
	unsigned char	*ptr1;
	unsigned char	*ptr2;
	size_t			i;

	ptr1 = (unsigned char *)s1;
	ptr2 = (unsigned char *)s2;
	i = -1;
	while (++i < len)
		if (ptr1[i] != ptr2[i])
			return (ptr1[i] - ptr2[i]);
	return (0);
}
